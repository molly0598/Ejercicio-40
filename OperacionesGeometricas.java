public class OperacionesGeometricas {
    public static double calcularRaiz(int valor) {
        return Math.round(Math.sqrt(valor));
    }

    public static int calcularAlCuadrado(int valor) {
        return valor * valor;
    }

    public static int calcularAlCubo(int valor) {
        return valor * valor * valor;
    }
}